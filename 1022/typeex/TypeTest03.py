"""
이스케이프(escape) 문자
\n      :new line. 문자열 안에서 줄을 바꿀 때 사용
\t      :tap.문자열 사이에 탭만큼의 간격을 줄 때 사용
\\      :문자 \를 그대로 표현할 때 사용
\'      :홑따옴표를 그대로 표현할 때 사용
\"      :쌍따옴표를 그대로 표현할 때 사용
"""
print("Ha\tHa\tHa")
print("역슬래시 \\")
print("쌍따옴표 \"")
print("홑따옴표 \'")

